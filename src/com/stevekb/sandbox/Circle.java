package com.stevekb.sandbox;

public class Circle {

	public float x, y, vx, vy;
	public int radius;
	public int color;

	public Circle(float x, float y, float vx, float vy, int radius, int color) {

		this.x = x;
		this.y = y;
		this.vx = vx;
		this.vy = vy;
		this.radius = radius;
		this.color = color;
	}
}
